public extension Int
{
	var ratingString:String
	{
		let n1 = self.clipped(to:0...5)
		let n2 = 5 - n1
		var str = ""
		
		for _ in 0..<n1
		{
			str += "★"
		}
		for _ in 0..<n2
		{
			str += "☆"
		}
		
		return str
	}
}


